/*
Faça um programa que calcule e mostre a 
área de um triângulo. Sabe-se que: Área = (base * altura)/2.
*/
var base = prompt("Digite aqui a base do triângulo: ");
console.log("A base do triângulo é de: " + base);
var altura = prompt("Digite aqui a altura do triângulo: ");
console.log("A altura do triângulo é de: " + altura);
var area = (base * altura) / 2;
console.log("A área do triângulo é de: " + area);