/*
Faça um programa que calcule e mostre a 
área de um círculo. Sabe-se que: Área = Pi * R², aonde Pi = 3,14.
*/
const pi = 3.14;
var raio = prompt("Digite aqui o raio do círculo: ");
console.log("O raio do círculo é de: " + raio);
var areaCirc = (pi * ((raio)**2));
console.log("A área do círculo é de: " + areaCirc);
