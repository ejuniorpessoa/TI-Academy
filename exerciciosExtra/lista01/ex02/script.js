/*
 Faça um programa que receba três notas, 
calcule e mostre a média aritmética entre elas. 
*/

var n1 = parseInt(prompt("Insira a 1º nota: "));
var n2 = parseInt(prompt("Insira a 2º nota: "));
var n3 = parseInt(prompt("Insira a 3º nota: "));

var media = (n1 + n2 + n3)/3;
console.log("A média aritmética é de: " + media );