/* 
Faça um programa que receba quatro números inteiros, calcule e mostre a soma desses números. 
*/
var n1 = parseInt(prompt("Insira o 1º número: "));
var n2 = parseInt(prompt("Insira o 2º número: "));
var n3 = parseInt(prompt("Insira o 3º número: "));
var n4 = parseInt(prompt("Insira o 4º número: "));

var s = n1 + n2 + n3 + n4;
console.log("O resultado é: " + s );